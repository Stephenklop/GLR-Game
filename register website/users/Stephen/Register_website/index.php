      <p>Het GLR Game Colosseum is een idee van Rezone Media. De eigenaren van dit bedrijf bestaan uit ex-GLR studenten, waarvan een van de eigenaar in zijn tijd op het GLR een game-toernooi had georganiseerd dat buiten schooltijd online plaatsvond. Tientallen enthousiaste studenten deden daaraan mee en dit werd een groot succes.<br><br>

Na de oprichting van Rezone Media in 2017 kwam het idee om een game-toernooi op het GLR te organiseren opnieuw naar voren. Dit keer met het idee om het toernooi in de entreehal van het GLR te organiseren. Daarvoor is toestemming van het GLR nodig en wij dachten dat het koppelen van een goed doel aan dit toernooi ervoor kan zorgen dat het groots kan uitpakken doordat dit een plaatsvervangend evenement is voor het voormalige GLR Serious Request-project.<br><br>

Met de hulp en steun van partners zoals AFK Esports, het GLR en enthousiaste GLR-studenten, hebben we dit project kunnen realiseren en zijn we er erg trots op dat de eerste editie van het GLR Game Colosseum van 18 t/m 20 december kan plaatsvinden.
